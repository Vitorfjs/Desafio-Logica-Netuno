package Model;

public class Avatar {
	
	private String corCabelo;
    private String estiloCabelo;
    private String corPele;
    private String corOlhos;
    private String altura;
    private String peso;
    private String tipoCorpo;
    private String roupa;
    private String montaria;
    
	public String getMontaria() {
		return montaria;
	}
	public void setMontaria(String montaria) {
		this.montaria = montaria;
	}
	public String getCorCabelo() {
		return corCabelo;
	}
	public void setCorCabelo(String corCabelo) {
		this.corCabelo = corCabelo;
	}
	public String getEstiloCabelo() {
		return estiloCabelo;
	}
	public void setEstiloCabelo(String estiloCabelo) {
		this.estiloCabelo = estiloCabelo;
	}
	public String getCorPele() {
		return corPele;
	}
	public void setCorPele(String corPele) {
		this.corPele = corPele;
	}
	public String getCorOlhos() {
		return corOlhos;
	}
	public void setCorOlhos(String corOlhos) {
		this.corOlhos = corOlhos;
	}
	public String getAltura() {
		return altura;
	}
	public void setAltura(String altura) {
		this.altura = altura;
	}
	public String getPeso() {
		return peso;
	}
	public void setPeso(String peso) {
		this.peso = peso;
	}
	public String getTipoCorpo() {
		return tipoCorpo;
	}
	public void setTipoCorpo(String tipoCorpo) {
		this.tipoCorpo = tipoCorpo;
	}
	public String getRoupa() {
		return roupa;
	}
	public void setRoupa(String roupa) {
		this.roupa = roupa;
	}
    
    

}
